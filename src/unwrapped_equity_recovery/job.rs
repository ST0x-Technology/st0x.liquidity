//! Apalis job that orchestrates unwrapped-equity recovery.
//!
//! Enqueued per symbol with a positive `BaseWalletUnwrappedEquity`
//! balance. The job is a thin orchestrator: it reads `InventoryView`,
//! picks a dispatch path, and sends the command sequence through the
//! aggregate's store. All side effects (raindex deposit/confirm,
//! wrapper wrap/confirm, mint/redemption resume) live in the
//! aggregate's command handlers via its `Services`.
//!
//! Steps:
//!
//! 1. Claim the `equity_in_progress` guard for the symbol. If another
//!    task already holds it, the job returns a retryable error so apalis
//!    re-attempts until the guard frees. It must NOT skip silently: the
//!    job's per-symbol idempotency key blocks the next inventory poll from
//!    re-enqueuing, so a silent skip would strand the orphan.
//! 2. Read the inventory view's wallet balance + active mint/redemption
//!    maps to pick a `DispatchDecision`.
//! 3. Send `Detect` followed by the path-specific command sequence:
//!    `DispatchToMint`, `DispatchToRedemption`, or `SubmitOrphanWrap +
//!    ConfirmOrphanWrap + SubmitOrphanDeposit + ConfirmOrphanDeposit`.
//!    The aggregate's terminal state encodes the outcome; no separate
//!    completion command is needed.
//!
//! The job carries its `UnwrappedEquityRecoveryId` in the apalis
//! payload (generated by the reactor when pushing the job) so retries
//! target the same aggregate -- the next command in the sequence picks
//! up wherever the previous attempt stopped.

