/// Apalis queue type for [`UnwrappedEquityRecoveryJob`].
pub(crate) type UnwrappedEquityRecoveryJobQueue = JobQueue<UnwrappedEquityRecoveryJob>;

/// Dependencies the recovery job needs to claim the per-symbol concurrency
/// guard, read inventory, perform the recovery's onchain/transfer side effects,
/// and record outcomes through the aggregate's pure command store.
pub(crate) struct UnwrappedEquityRecoveryCtx {
    pub(crate) inventory: Arc<BroadcastingInventory>,
    pub(crate) store: Arc<Store<UnwrappedEquityRecovery>>,
    /// Onchain/transfer dependencies the job drives before recording outcomes.
    pub(crate) services: UnwrappedEquityRecoveryServices,
    pub(crate) mint_store: Arc<Store<TokenizedEquityMint>>,
    pub(crate) redemption_store: Arc<Store<EquityRedemption>>,
    pub(crate) equity_in_progress: Arc<RwLock<HashMap<Symbol, GuardState>>>,
    /// Queue the job re-pushes itself onto when it loses the per-symbol
    /// guard, so a skipped recovery is re-dispatched rather than stranded.
    pub(crate) queue: UnwrappedEquityRecoveryJobQueue,
    /// Delay before a guard-contention reschedule re-attempts. Sourced from
    /// the configured inventory poll interval.
    pub(crate) reschedule_interval: Duration,
}

/// Why a single recovery job attempt failed. Errors propagate up through
/// apalis. Terminal service failures are recorded as `RecoveryFailed` via
/// `fail_recovery`; retryable service failures surface here as
/// `RetryableWrapConfirmation`/`RetryableDepositConfirmation`/`NodeSyncFailed`
/// so apalis retries.
#[derive(Debug, Error)]
pub(crate) enum UnwrappedEquityRecoveryJobError {
    #[error("recovery aggregate error: {0}")]
    Aggregate(#[from] SendError<UnwrappedEquityRecovery>),
    #[error(transparent)]
    Domain(#[from] UnwrappedEquityRecoveryError),
    #[error(
        "active mint {mint_id} for symbol {symbol} carries quantity {aggregate_shares}, \
         which does not match the unwrapped-equity wallet snapshot {snapshot_shares} -- \
         refusing to dispatch to a stale aggregate"
    )]
    ActiveMintQuantityMismatch {
        symbol: Symbol,
        mint_id: IssuerRequestId,
        snapshot_shares: FractionalShares,
        aggregate_shares: FractionalShares,
    },
    #[error(
        "active redemption {redemption_id} for symbol {symbol} carries quantity \
         {aggregate_shares}, which does not match the unwrapped-equity wallet snapshot \
         {snapshot_shares} -- refusing to dispatch to a stale aggregate"
    )]
    ActiveRedemptionQuantityMismatch {
        symbol: Symbol,
        redemption_id: RedemptionAggregateId,
        snapshot_shares: FractionalShares,
        aggregate_shares: FractionalShares,
    },
    #[error("active mint {mint_id} for symbol {symbol} is missing from the mint store")]
    ActiveMintMissing {
        symbol: Symbol,
        mint_id: IssuerRequestId,
    },
    #[error(
        "active redemption {redemption_id} for symbol {symbol} is missing from the \
         redemption store"
    )]
    ActiveRedemptionMissing {
        symbol: Symbol,
        redemption_id: RedemptionAggregateId,
    },
    #[error("active mint store error: {0}")]
    ActiveMintAggregate(
        #[from]
        st0x_event_sorcery::AggregateError<
            st0x_event_sorcery::LifecycleError<crate::tokenized_equity_mint::TokenizedEquityMint>,
        >,
    ),
    #[error("active redemption store error: {0}")]
    ActiveRedemptionAggregate(
        #[from]
        st0x_event_sorcery::AggregateError<
            st0x_event_sorcery::LifecycleError<crate::equity_redemption::EquityRedemption>,
        >,
    ),
    #[error("failed to reschedule recovery job after guard contention: {0}")]
    Reschedule(#[from] QueuePushError),
    #[error(
        "no positive unwrapped balance in inventory view for symbol {symbol}; \
         guard drop restores HeldForRecovery or removes orphan entry"
    )]
    NoBalanceSkip { symbol: Symbol },
    #[error("wrap confirmation for tx {wrap_tx_hash} is retryable")]
    RetryableWrapConfirmation { wrap_tx_hash: TxHash },
    #[error("deposit confirmation for tx {vault_deposit_tx_hash} is retryable")]
    RetryableDepositConfirmation { vault_deposit_tx_hash: TxHash },
    #[error("RPC node did not catch up to wrap block {required_block} after {attempts} polls")]
    NodeSyncFailed { required_block: u64, attempts: u32 },
}

/// Apalis job payload. The reactor pushes one of these per symbol with a
/// positive `BaseWalletUnwrappedEquity` balance, generating a fresh
/// `recovery_id` at push time so apalis retries hit the same aggregate.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub(crate) struct UnwrappedEquityRecoveryJob {
    pub(crate) symbol: Symbol,
    pub(crate) recovery_id: UnwrappedEquityRecoveryId,
}

impl Job<UnwrappedEquityRecoveryCtx> for UnwrappedEquityRecoveryJob {
    type Output = ();
    type Error = UnwrappedEquityRecoveryJobError;

    const WORKER_NAME: &'static str = "unwrapped-equity-recovery-worker";

    #[cfg(any(test, feature = "test-support"))]
    const JOB_KIND: crate::conductor::job::JobKind =
        crate::conductor::job::JobKind::UnwrappedEquityRecovery;

    fn label(&self) -> Label {
        Label::new(format!(
            "UnwrappedEquityRecovery:{}:{}",
            self.symbol, self.recovery_id
        ))
    }

    async fn perform(&self, ctx: &UnwrappedEquityRecoveryCtx) -> Result<Self::Output, Self::Error> {
        let symbol = self.symbol.clone();

        // Two-path guard claim:
        // 1. `HeldForRecovery` -> `ActiveTransfer`: claim from a handoff set by
        //    the transfer job after a PostReceipt error. When the mint aggregate
        //    is in TokensReceived or WrapSubmitted, the ERC-4626 wrap reverted,
        //    tokens are UNWRAPPED in the base wallet, and this job is the correct
        //    deadlock-break claimant. (WrappedEquityRecovery handles the deposit-
        //    failed path when the aggregate is in TokensWrapped.)
        // 2. Absent -> `ActiveTransfer`: orphan path -- no active transfer was
        //    in progress; inventory detected a wallet balance independently.
        // 3. `ActiveTransfer`: a live transfer job owns the slot; reschedule.
        let Some(guard) = claim_guard_for_recovery_or_orphan(&ctx.equity_in_progress, &symbol)
        else {
            debug!(
                target: "rebalance",
                %symbol,
                recovery_id = %self.recovery_id,
                "Rescheduling unwrapped equity recovery: equity_in_progress held by active transfer",
            );
            ctx.queue
                .clone()
                .push_with_delay(self.clone(), ctx.reschedule_interval)
                .await?;
            return Ok(());
        };

        let current_state = ctx.store.load(&self.recovery_id).await?;

        if let Some(state) = &current_state
            && state.is_terminal()
        {
            debug!(
                target: "rebalance",
                %symbol,
                recovery_id = %self.recovery_id,
                "Skipping unwrapped equity recovery: aggregate already terminal",
            );
            guard.release();
            return Ok(());
        }

        let result = match current_state {
            None => start_from_detect(ctx, &self.recovery_id, &symbol).await,
            Some(UnwrappedEquityRecovery::Detected { shares, .. }) => {
                resume_from_detected(ctx, &self.recovery_id, &symbol, shares).await
            }
            Some(UnwrappedEquityRecovery::OrphanWrapSubmitted { wrap_tx_hash, .. }) => {
                resume_from_confirm_wrap(ctx, &self.recovery_id, &symbol, wrap_tx_hash).await
            }
            Some(UnwrappedEquityRecovery::OrphanWrapped {
                wrapped_amount,
                wrap_block,
                ..
            }) => {
                resume_from_submit_deposit(
                    ctx,
                    &self.recovery_id,
                    &symbol,
                    wrapped_amount,
                    wrap_block,
                )
                .await
            }
            Some(UnwrappedEquityRecovery::OrphanDepositSubmitted {
                vault_deposit_tx_hash,
                ..
            }) => resume_from_confirm_deposit(ctx, &self.recovery_id, vault_deposit_tx_hash).await,
            // Terminal states are already short-circuited by the `is_terminal`
            // early-return above; they are enumerated explicitly here (rather
            // than a `_` arm) so that adding a new non-terminal state fails to
            // compile until it is wired into a resume path, instead of silently
            // falling through to a no-op that never drives the recovery forward.
            Some(
                UnwrappedEquityRecovery::DispatchedToMint { .. }
                | UnwrappedEquityRecovery::DispatchedToRedemption { .. }
                | UnwrappedEquityRecovery::OrphanDeposited { .. }
                | UnwrappedEquityRecovery::Failed { .. },
            ) => Ok(()),
        };

        // Single exhaustive match on result:
        // - Ok(()): release guard (removes slot); recovery completed.
        // - Err(NoBalanceSkip): map to Ok(()) so apalis does NOT retry; guard
        //   drops normally (HeldForRecovery origin restores to HeldForRecovery;
        //   orphan removes). A HeldForRecovery-origin skip self-reschedules
        //   (push_with_delay) since the reactor only re-dispatches on a positive
        //   balance; an orphan skip relies on that next positive-balance poll.
        // - Err(other): propagate; guard drops, restoring prior state.
        match result {
            Ok(()) => {
                guard.release();
                Ok(())
            }
            Err(UnwrappedEquityRecoveryJobError::NoBalanceSkip { symbol: ref sym }) => {
                // No unwrapped balance to drain this cycle. The reactor only
                // re-dispatches recovery on a POSITIVE balance, so a
                // HeldForRecovery-origin claim that simply drops here would
                // restore HeldForRecovery with no job left to drain it --
                // wedging the symbol if the tokens left the wallet out of band.
                // Re-enqueue with a delay to keep polling. An orphan claim
                // instead drops to absent, so the next positive-balance poll
                // re-triggers it without help.
                if guard.claimed_from_held_for_recovery() {
                    debug!(
                        target: "rebalance",
                        symbol = %sym,
                        recovery_id = %self.recovery_id,
                        "Rescheduling unwrapped equity recovery: no balance yet but slot is \
                         HeldForRecovery; re-enqueueing with delay to keep polling until the \
                         balance resolves or the slot is cleared",
                    );
                    ctx.queue
                        .clone()
                        .push_with_delay(self.clone(), ctx.reschedule_interval)
                        .await?;
                } else {
                    info!(
                        target: "rebalance",
                        symbol = %sym,
                        "Unwrapped equity recovery: no balance in inventory view; orphan \
                         claim, guard drop removes the entry; next positive-balance poll \
                         re-dispatches",
                    );
                }
                Ok(())
            }
            Err(other) => Err(other),
        }
    }
}

/// Fresh recovery: read inventory, validate active-aggregate quantity, send
/// `Detect`, then dispatch from `Detected`.
async fn start_from_detect(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    let Some(snapshot) = read_recovery_snapshot(&ctx.inventory, symbol).await else {
        debug!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            "Skipping unwrapped equity recovery: no positive balance in inventory view; \
             guard drop restores HeldForRecovery or removes orphan entry",
        );
        // Return Err so the caller does NOT call guard.release(). Drop restores
        // HeldForRecovery (if claimed from handoff) or removes the entry (orphan),
        // letting the next inventory poll re-trigger recovery once tokens are visible.
        return Err(UnwrappedEquityRecoveryJobError::NoBalanceSkip {
            symbol: symbol.clone(),
        });
    };

    validate_active_aggregate_quantity(ctx, symbol, &snapshot).await?;

    ctx.store
        .send(
            recovery_id,
            UnwrappedEquityRecoveryCommand::Detect {
                symbol: symbol.clone(),
                shares: snapshot.shares,
            },
        )
        .await?;

    info!(
        target: "rebalance",
        %symbol,
        %recovery_id,
        shares = %snapshot.shares,
        ?snapshot.dispatch,
        "Unwrapped equity recovery: dispatched detection",
    );

    dispatch_from_detected(ctx, recovery_id, symbol, snapshot.shares, snapshot.dispatch).await
}

/// Resume from `Detected`: a previous attempt persisted `Detect` but crashed
/// before any dispatch command landed. Re-read inventory and dispatch.
async fn resume_from_detected(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
    detected_shares: FractionalShares,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    let Some(snapshot) = read_recovery_snapshot(&ctx.inventory, symbol).await else {
        // Detected was persisted but the inventory no longer reports a positive
        // balance. The recovered tokens are either gone (recovered out-of-band)
        // or the previous snapshot was stale. Fail the aggregate so it leaves
        // the in-progress slot and a subsequent poll can start fresh.
        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            "Resuming unwrapped recovery from Detected but inventory now reports zero balance; \
             failing the aggregate to free the symbol",
        );
        ctx.store
            .send(
                recovery_id,
                UnwrappedEquityRecoveryCommand::FailRecovery {
                    reason: "inventory reports zero unwrapped balance on resume from Detected"
                        .to_string(),
                },
            )
            .await?;
        return Ok(());
    };

    if snapshot.shares != detected_shares {
        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            snapshot_shares = %snapshot.shares,
            %detected_shares,
            "Resuming unwrapped recovery from Detected but inventory shares changed; \
             failing the stale aggregate to allow a fresh recovery",
        );
        ctx.store
            .send(
                recovery_id,
                UnwrappedEquityRecoveryCommand::FailRecovery {
                    reason: format!(
                        "inventory shares changed on resume from Detected: detected \
                         {detected_shares}, current {}",
                        snapshot.shares
                    ),
                },
            )
            .await?;
        return Ok(());
    }

    // Mirror `start_from_detect`: a crash can land here after the inventory has
    // shifted, so re-validate the active mint/redemption quantity against the
    // fresh snapshot before dispatching rather than trusting the persisted
    // `Detected` state. Without this, the resume path would dispatch on a stale
    // aggregate that the fresh path would have rejected.
    if let Err(error) = validate_active_aggregate_quantity(ctx, symbol, &snapshot).await {
        if !is_business_validation_error(&error) {
            return Err(error);
        }

        // The persisted `Detected` aggregate's premise is stale: the active
        // mint/redemption no longer matches the wallet snapshot. Terminalize it
        // (mirroring the conflict branch) and return Ok, matching
        // `start_from_detect` for the identical failure: the aggregate is
        // already terminal, so an Err here would only burn an apalis retry
        // slot on a job with nothing left to do. A fresh poll then re-detects
        // from current inventory.
        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            %error,
            "Unwrapped equity recovery: active aggregate quantity mismatch on resume; failing recovery",
        );
        ctx.store
            .send(
                recovery_id,
                UnwrappedEquityRecoveryCommand::FailRecovery {
                    reason: format!("active aggregate validation failed on resume: {error}"),
                },
            )
            .await?;
        return Ok(());
    }

    info!(
        target: "rebalance",
        %symbol,
        %recovery_id,
        ?snapshot.dispatch,
        "Resuming unwrapped recovery from Detected",
    );

    dispatch_from_detected(ctx, recovery_id, symbol, snapshot.shares, snapshot.dispatch).await
}

/// Records a terminal recovery failure as a `RecoveryFailed` event. Returns
/// `Ok(())` so the caller stops the chain without burning an apalis retry on an
/// already-terminal aggregate.
async fn fail_recovery(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    reason: String,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    ctx.store
        .send(
            recovery_id,
            UnwrappedEquityRecoveryCommand::FailRecovery { reason },
        )
        .await?;
    Ok(())
}

/// Performs the dispatch step's I/O and records the outcome. For active
/// mint/redemption, resumes the transfer and records the dispatch (or
/// `RecoveryFailed`); for the orphan path, chains into the four-step sequence;
/// for conflict, fails the aggregate.
async fn dispatch_from_detected(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
    shares: FractionalShares,
    dispatch: DispatchDecision,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    match dispatch {
        DispatchDecision::ActiveMint(mint_id) => {
            match ctx.services.transfer.resume_mint(&mint_id).await {
                Ok(()) => {
                    info!(target: "rebalance", %mint_id, "Unwrapped equity recovery: resume_mint succeeded");
                    ctx.store
                        .send(
                            recovery_id,
                            UnwrappedEquityRecoveryCommand::DispatchToMint { mint_id },
                        )
                        .await?;
                    Ok(())
                }
                Err(error) => {
                    warn!(target: "rebalance", %mint_id, ?error, "Unwrapped equity recovery: resume_mint failed");
                    fail_recovery(ctx, recovery_id, format!("resume_mint failed: {error}")).await
                }
            }
        }
        DispatchDecision::ActiveRedemption(redemption_id) => {
            match ctx
                .services
                .transfer
                .resume_redemption(&redemption_id)
                .await
            {
                Ok(()) => {
                    info!(target: "rebalance", %redemption_id, "Unwrapped equity recovery: resume_redemption succeeded");
                    ctx.store
                        .send(
                            recovery_id,
                            UnwrappedEquityRecoveryCommand::DispatchToRedemption { redemption_id },
                        )
                        .await?;
                    Ok(())
                }
                Err(error) => {
                    warn!(target: "rebalance", %redemption_id, ?error, "Unwrapped equity recovery: resume_redemption failed");
                    fail_recovery(
                        ctx,
                        recovery_id,
                        format!("resume_redemption failed: {error}"),
                    )
                    .await
                }
            }
        }
        DispatchDecision::Orphan => resume_from_submit_wrap(ctx, recovery_id, symbol, shares).await,
        DispatchDecision::Conflict {
            mint_id,
            redemption_id,
        } => {
            let reason = format!(
                "InventoryView reports active mint {mint_id} and active redemption \
                 {redemption_id} for symbol {symbol}; refusing to dispatch"
            );
            warn!(target: "rebalance", %symbol, %mint_id, %redemption_id, "Unwrapped equity recovery: conflict");
            fail_recovery(ctx, recovery_id, reason).await
        }
    }
}

fn is_business_validation_error(error: &UnwrappedEquityRecoveryJobError) -> bool {
    match error {
        UnwrappedEquityRecoveryJobError::ActiveMintQuantityMismatch { .. }
        | UnwrappedEquityRecoveryJobError::ActiveRedemptionQuantityMismatch { .. }
        | UnwrappedEquityRecoveryJobError::ActiveMintMissing { .. }
        | UnwrappedEquityRecoveryJobError::ActiveRedemptionMissing { .. } => true,
        UnwrappedEquityRecoveryJobError::Aggregate(_)
        | UnwrappedEquityRecoveryJobError::Domain(_)
        | UnwrappedEquityRecoveryJobError::ActiveMintAggregate(_)
        | UnwrappedEquityRecoveryJobError::ActiveRedemptionAggregate(_)
        | UnwrappedEquityRecoveryJobError::Reschedule(_)
        | UnwrappedEquityRecoveryJobError::RetryableWrapConfirmation { .. }
        | UnwrappedEquityRecoveryJobError::RetryableDepositConfirmation { .. }
        | UnwrappedEquityRecoveryJobError::NodeSyncFailed { .. }
        | UnwrappedEquityRecoveryJobError::NoBalanceSkip { .. } => false,
    }
}

/// Orphan path step 1: resolves the wrapped token, wraps the bot wallet's
/// underlying tokens, records the submitted tx hash, then chains to confirm.
/// A lookup/conversion/submit failure is terminal (`RecoveryFailed`).
async fn resume_from_submit_wrap(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
    shares: FractionalShares,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    let underlying_amount = match shares.to_u256_18_decimals() {
        Ok(raw) => raw,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: shares conversion failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("shares conversion failed: {error}"),
            )
            .await;
        }
    };

    let wrapped_token = match ctx.services.wrapper.lookup_derivative(symbol) {
        Ok(token) => token,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: lookup_derivative failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("wrapper.lookup_derivative failed: {error}"),
            )
            .await;
        }
    };

    match ctx
        .services
        .wrapper
        .submit_wrap(wrapped_token, underlying_amount, ctx.services.wallet)
        .await
    {
        Ok(wrap_tx_hash) => {
            info!(target: "rebalance", %symbol, %wrap_tx_hash, "Unwrapped equity recovery: submit_wrap succeeded");
            ctx.store
                .send(
                    recovery_id,
                    UnwrappedEquityRecoveryCommand::SubmitOrphanWrap { wrap_tx_hash },
                )
                .await?;
            resume_from_confirm_wrap(ctx, recovery_id, symbol, wrap_tx_hash).await
        }
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: submit_wrap failed");
            fail_recovery(
                ctx,
                recovery_id,
                format!("wrapper.submit_wrap failed: {error}"),
            )
            .await
        }
    }
}

/// Orphan path step 2: confirms the wrap, records the minted amount and block,
/// then chains to deposit. A confirmed-missing/dropped receipt is terminal; any
/// other confirmation error is retryable (the job error propagates so apalis
/// retries from the persisted `OrphanWrapSubmitted` state).
async fn resume_from_confirm_wrap(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
    wrap_tx_hash: TxHash,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    let wrapped_token = match ctx.services.wrapper.lookup_derivative(symbol) {
        Ok(token) => token,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: lookup_derivative failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("wrapper.lookup_derivative failed: {error}"),
            )
            .await;
        }
    };

    match ctx
        .services
        .wrapper
        .confirm_wrap(wrapped_token, wrap_tx_hash)
        .await
    {
        Ok(WrapConfirmation {
            shares: wrapped_amount,
            block: wrap_block,
        }) => {
            info!(target: "rebalance", %symbol, %wrap_tx_hash, %wrapped_amount, "Unwrapped equity recovery: confirm_wrap succeeded");
            ctx.store
                .send(
                    recovery_id,
                    UnwrappedEquityRecoveryCommand::ConfirmOrphanWrap {
                        wrapped_amount,
                        wrap_block,
                    },
                )
                .await?;
            resume_from_submit_deposit(ctx, recovery_id, symbol, wrapped_amount, Some(wrap_block))
                .await
        }
        Err(error) => {
            warn!(target: "rebalance", %symbol, %wrap_tx_hash, ?error, "Unwrapped equity recovery: confirm_wrap failed");
            match error {
                // Terminal: the tx will not succeed on retry (malformed receipt,
                // dropped tx, config/drift). Record `RecoveryFailed` for the audit
                // trail instead of looping on apalis.
                WrapperError::MissingDepositEvent
                | WrapperError::MissingWithdrawEvent
                | WrapperError::SymbolNotConfigured(_)
                | WrapperError::RedeemExceedsMax { .. }
                | WrapperError::Evm(EvmError::TransactionDropped { .. }) => {
                    fail_recovery(
                        ctx,
                        recovery_id,
                        format!("wrapper.confirm_wrap failed: {error}"),
                    )
                    .await
                }
                // Transient: RPC transport/lag, contract view, ratio read, or a
                // not-yet-finalized receipt -- apalis re-drives the confirm from
                // the persisted state. Enumerated (no `_`) so a new WrapperError
                // variant forces a terminal-vs-retryable decision here.
                WrapperError::Evm(_)
                | WrapperError::Contract(_)
                | WrapperError::Ratio(_)
                | WrapperError::MissingBlockNumber { .. } => {
                    Err(UnwrappedEquityRecoveryJobError::RetryableWrapConfirmation { wrap_tx_hash })
                }
            }
        }
    }
}

/// Orphan path step 3: waits for the wrap block (skipped for legacy aggregates
/// persisted before `wrap_block` existed), resolves the vault, deposits the
/// wrapped shares, records the tx hash, then chains to confirm. A wait failure
/// is retryable; a lookup/deposit failure is terminal.
async fn resume_from_submit_deposit(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    symbol: &Symbol,
    wrapped_amount: U256,
    wrap_block: Option<u64>,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    if let Some(block) = wrap_block {
        ctx.services
            .wrapper
            .wait_for_block(block)
            .await
            .inspect_err(|error| {
                warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: wait_for_block failed");
            })
            .map_err(|error| UnwrappedEquityRecoveryJobError::NodeSyncFailed {
                required_block: block,
                attempts: node_sync_attempts(&error),
            })?;
    }

    let wrapped_token = match ctx.services.wrapper.lookup_derivative(symbol) {
        Ok(token) => token,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: lookup_derivative failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("wrapper.lookup_derivative failed: {error}"),
            )
            .await;
        }
    };

    let vault_id = match ctx
        .services
        .vault_lookup
        .vault_id_for_token(wrapped_token)
        .await
    {
        Ok(id) => id,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: vault_id_for_token failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("vault_lookup.vault_id_for_token failed: {error}"),
            )
            .await;
        }
    };

    // `wrapped_amount` is the raw ERC-4626 share amount from the wrap's Deposit
    // event. Wrapped equity tokens (wtSTOCK) are minted at the system-wide
    // tokenized-equity precision -- `TOKENIZED_EQUITY_DECIMALS` (18), the same
    // standard the tSTOCK underlying and `FractionalShares` use -- so the raw
    // amount is already in 18-decimal units and is interpreted as such.
    match ctx
        .services
        .raindex
        .submit_deposit(
            wrapped_token,
            vault_id,
            wrapped_amount,
            TOKENIZED_EQUITY_DECIMALS,
        )
        .await
    {
        Ok(vault_deposit_tx_hash) => {
            info!(target: "rebalance", %symbol, %vault_deposit_tx_hash, "Unwrapped equity recovery: submit_deposit succeeded");
            ctx.store
                .send(
                    recovery_id,
                    UnwrappedEquityRecoveryCommand::SubmitOrphanDeposit {
                        vault_deposit_tx_hash,
                    },
                )
                .await?;
            resume_from_confirm_deposit(ctx, recovery_id, vault_deposit_tx_hash).await
        }
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Unwrapped equity recovery: submit_deposit failed");
            fail_recovery(
                ctx,
                recovery_id,
                format!("raindex.submit_deposit failed: {error}"),
            )
            .await
        }
    }
}

/// Orphan path step 4: confirms the Raindex deposit and records it. A dropped
/// tx is terminal; any other confirmation error is retryable.
async fn resume_from_confirm_deposit(
    ctx: &UnwrappedEquityRecoveryCtx,
    recovery_id: &UnwrappedEquityRecoveryId,
    vault_deposit_tx_hash: TxHash,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    match ctx.services.raindex.confirm_tx(vault_deposit_tx_hash).await {
        Ok(()) => {
            info!(target: "rebalance", %vault_deposit_tx_hash, "Unwrapped equity recovery: confirm_tx succeeded");
            ctx.store
                .send(
                    recovery_id,
                    UnwrappedEquityRecoveryCommand::ConfirmOrphanDeposit,
                )
                .await
                .map_err(Into::into)
        }
        Err(error) => {
            warn!(target: "rebalance", %vault_deposit_tx_hash, ?error, "Unwrapped equity recovery: confirm_tx failed");
            if error.is_transaction_dropped() {
                return fail_recovery(
                    ctx,
                    recovery_id,
                    format!("raindex.confirm_tx failed: {error}"),
                )
                .await;
            }
            Err(
                UnwrappedEquityRecoveryJobError::RetryableDepositConfirmation {
                    vault_deposit_tx_hash,
                },
            )
        }
    }
}

struct RecoverySnapshot {
    shares: FractionalShares,
    dispatch: DispatchDecision,
}

#[derive(Debug)]
enum DispatchDecision {
    ActiveMint(IssuerRequestId),
    ActiveRedemption(RedemptionAggregateId),
    Orphan,
    /// `InventoryView` reports both an active mint and an active redemption
    /// for the same symbol. A symbol can only be in one in-flight transfer
    /// at a time -- this is a corrupt-state signal, not a normal dispatch
    /// path.
    Conflict {
        mint_id: IssuerRequestId,
        redemption_id: RedemptionAggregateId,
    },
}

async fn read_recovery_snapshot(
    inventory: &BroadcastingInventory,
    symbol: &Symbol,
) -> Option<RecoverySnapshot> {
    let view = inventory.read().await;
    let shares = view.inflight_equity_at(symbol, InFlightEquityLocation::BaseWalletUnwrapped)?;
    if shares == FractionalShares::ZERO {
        return None;
    }
    let dispatch = decide_dispatch(&view, symbol);
    drop(view);
    Some(RecoverySnapshot { shares, dispatch })
}

/// Confirms the active mint or redemption aggregate carries the same quantity
/// as the unwrapped wallet snapshot. Without this check, the recovery stream
/// would record `snapshot.shares` for an aggregate that was actually set up
/// for a different amount, leaving the audit trail and the resumed work
/// disagreeing on how many shares moved.
async fn validate_active_aggregate_quantity(
    ctx: &UnwrappedEquityRecoveryCtx,
    symbol: &Symbol,
    snapshot: &RecoverySnapshot,
) -> Result<(), UnwrappedEquityRecoveryJobError> {
    match &snapshot.dispatch {
        DispatchDecision::ActiveMint(mint_id) => {
            let mint = ctx.mint_store.load(mint_id).await?.ok_or_else(|| {
                UnwrappedEquityRecoveryJobError::ActiveMintMissing {
                    symbol: symbol.clone(),
                    mint_id: mint_id.clone(),
                }
            })?;
            let aggregate_shares = FractionalShares::new(mint.quantity());
            if aggregate_shares != snapshot.shares {
                return Err(
                    UnwrappedEquityRecoveryJobError::ActiveMintQuantityMismatch {
                        symbol: symbol.clone(),
                        mint_id: mint_id.clone(),
                        snapshot_shares: snapshot.shares,
                        aggregate_shares,
                    },
                );
            }
            Ok(())
        }
        DispatchDecision::ActiveRedemption(redemption_id) => {
            let redemption = ctx
                .redemption_store
                .load(redemption_id)
                .await?
                .ok_or_else(
                    || UnwrappedEquityRecoveryJobError::ActiveRedemptionMissing {
                        symbol: symbol.clone(),
                        redemption_id: redemption_id.clone(),
                    },
                )?;
            let aggregate_shares = FractionalShares::new(redemption.quantity());
            if aggregate_shares != snapshot.shares {
                return Err(
                    UnwrappedEquityRecoveryJobError::ActiveRedemptionQuantityMismatch {
                        symbol: symbol.clone(),
                        redemption_id: redemption_id.clone(),
                        snapshot_shares: snapshot.shares,
                        aggregate_shares,
                    },
                );
            }
            Ok(())
        }
        DispatchDecision::Orphan | DispatchDecision::Conflict { .. } => Ok(()),
    }
}

fn decide_dispatch(view: &InventoryView, symbol: &Symbol) -> DispatchDecision {
    match (view.active_mint(symbol), view.active_redemption(symbol)) {
        (Some(mint_id), Some(redemption_id)) => DispatchDecision::Conflict {
            mint_id: mint_id.clone(),
            redemption_id: redemption_id.clone(),
        },
        (Some(mint_id), None) => DispatchDecision::ActiveMint(mint_id.clone()),
        (None, Some(redemption_id)) => DispatchDecision::ActiveRedemption(redemption_id.clone()),
        (None, None) => DispatchDecision::Orphan,
    }
}

