//! Apalis job that orchestrates unwrapped-equity recovery.
//!
//! Enqueued per symbol with a positive `BaseWalletUnwrappedEquity`
//! balance. The job owns the recovery's side effects: it reads
//! `InventoryView`, picks a dispatch path, and drives the onchain/transfer
//! work (raindex deposit/confirm, wrapper wrap/confirm, mint/redemption
//! resume) directly via `ctx.services`, recording each outcome through the
//! aggregate's pure recording commands. The aggregate's handlers are pure
//! event recorders (`type Services = ()`).
//!
//! Steps:
//!
//! 1. Claim the `equity_in_progress` guard for the symbol. If another
//!    task already holds it, the job reschedules itself with a delay
//!    (`push_with_delay`) and returns `Ok(())` rather than erroring --
//!    exhausting the apalis retry budget on a held guard fires the shared
//!    failure-notify and halts the whole monitor. The self-reschedule is
//!    required because a bare skip would strand the orphan: the inventory
//!    snapshot only re-emits `BaseWalletUnwrappedEquity` on a balance
//!    CHANGE, so an unchanged orphan balance has nothing to re-trigger the
//!    reactor. Rescheduling re-dispatches a fresh attempt once the guard
//!    frees, without consuming the retry budget.
//! 2. Read the inventory view's wallet balance + active mint/redemption
//!    maps to pick a `DispatchDecision`.
//! 3. Send `Detect` followed by the path-specific command sequence:
//!    `DispatchToMint`, `DispatchToRedemption`, or `SubmitOrphanWrap +
//!    ConfirmOrphanWrap + SubmitOrphanDeposit + ConfirmOrphanDeposit`.
//!    The aggregate's terminal state encodes the outcome; no separate
//!    completion command is needed.
//!
//! The job carries its `UnwrappedEquityRecoveryId` in the apalis
//! payload (generated by the reactor when pushing the job) so retries
//! target the same aggregate -- the next command in the sequence picks
//! up wherever the previous attempt stopped.

