    /// Job-level routing: an active mint for the symbol resumes the mint and
    /// records `DispatchToMint` -- not the orphan deposit path.
    #[tokio::test]
    async fn perform_drives_active_mint_path_to_dispatched_to_mint() {
        let symbol = aapl();
        let mint_id = issuer_request_id("ISS001");
        let view = view_with_wrapped_balance(&symbol, five_shares())
            .set_active_mint(symbol.clone(), mint_id.clone());
        let ctx = test_ctx(view, HashMap::new()).await;

        // Seed the mint to `TokensWrapped` so `resume_mint` runs to completion
        // under the succeeding mocks.
        ctx.mint_store
            .send(
                &mint_id,
                TokenizedEquityMintCommand::RecordMintRequested {
                    issuer_request_id: mint_id.clone(),
                    symbol: symbol.clone(),
                    quantity: float!(5),
                    wallet: Address::random(),
                    tokenization_request_id: tokenization_request_id("tok-req-dispatch"),
                },
            )
            .await
            .unwrap();
        ctx.mint_store
            .send(
                &mint_id,
                TokenizedEquityMintCommand::RecordTokensReceived {
                    tx_hash: Some(TxHash::repeat_byte(0x11)),
                    token_symbol: Some(format!("t{symbol}")),
                    fees: None,
                },
            )
            .await
            .unwrap();
        let wrap_tx = TxHash::repeat_byte(0x22);
        ctx.mint_store
            .send(
                &mint_id,
                TokenizedEquityMintCommand::SubmitWrap {
                    wrap_tx_hash: wrap_tx,
                },
            )
            .await
            .unwrap();
        ctx.mint_store
            .send(
                &mint_id,
                TokenizedEquityMintCommand::WrapTokens {
                    wrap_tx_hash: wrap_tx,
                    wrapped_shares: U256::from(5_000_000_000_000_000_000u128),
                    wrap_block: 1,
                },
            )
            .await
            .unwrap();

        let job = WrappedEquityRecoveryJob {
            symbol: symbol.clone(),
            recovery_id: WrappedEquityRecoveryId(Uuid::new_v4()),
        };
        job.perform(&ctx)
            .await
            .expect("active-mint recovery should run to completion");

        let state = ctx.store.load(&job.recovery_id).await.unwrap();
        let Some(WrappedEquityRecovery::DispatchedToMint {
            mint_id: dispatched,
            ..
        }) = state
        else {
            panic!("active mint must route to DispatchedToMint, got {state:?}");
        };
        assert_eq!(
            dispatched, mint_id,
            "the dispatched mint id must be the active mint from the inventory",
        );

        let mint = ctx.mint_store.load(&mint_id).await.unwrap();
        assert!(
            matches!(mint, Some(TokenizedEquityMint::DepositedIntoRaindex { .. })),
            "resume_mint must drive the seeded mint to completion, got {mint:?}",
        );
    }

