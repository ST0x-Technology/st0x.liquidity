//! Apalis job that orchestrates wrapped-equity recovery.
//!
//! Enqueued per symbol with a positive `BaseWalletWrappedEquity` balance.
//! The job performs the recovery's side effects (raindex deposit/confirm,
//! mint/redemption resume, wrapped-token lookup) and records each outcome
//! through the aggregate's pure commands. The aggregate is a pure state
//! machine (`Services = ()`); all I/O lives here.
//!
//! Steps:
//!
//! 1. Claim the `equity_in_progress` guard for the symbol (from absent or
//!    `HeldForRecovery`). If a live transfer owns it (`ActiveTransfer`), the
//!    job reschedules itself with a delay so an unchanged wallet balance is
//!    not stranded waiting for another snapshot.
//! 2. Resume from the persisted aggregate state so a retry picks up where the
//!    previous attempt stopped: fresh (`Detect` -> dispatch), `Detected`
//!    (re-dispatch), or `OrphanDepositSubmitted` (re-confirm the deposit).
//!    Resuming from the persisted deposit tx hash rather than re-running
//!    `submit_deposit` is what keeps a retry from double-depositing -- but only
//!    once `OrphanDepositSubmitted` has landed. A crash after `submit_deposit`
//!    returns but before that event is persisted re-runs `submit_deposit` on
//!    resume (a second on-chain deposit); this dual-write window is
//!    architecturally unavoidable and matches the prior atomic-handler design.
//! 3. For the chosen dispatch path, run the I/O and send the matching pure
//!    command: `DispatchToMint` / `DispatchToRedemption`, or the orphan
//!    `SubmitOrphanDeposit { tx } -> ConfirmOrphanDeposit` sequence. A terminal
//!    service failure is recorded via `FailRecovery`; a transient deposit
//!    confirmation failure surfaces as a job error so apalis retries from the
//!    persisted `OrphanDepositSubmitted`.
//!
//! The job carries its `WrappedEquityRecoveryId` in the apalis payload
//! (generated by the reactor when pushing the job) so retries target the
//! same aggregate.

