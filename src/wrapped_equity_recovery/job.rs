//! Apalis job that orchestrates wrapped-equity recovery.
//!
//! Enqueued per symbol with a positive `BaseWalletWrappedEquity` balance.
//! The job is a thin orchestrator: it reads `InventoryView`, picks a
//! dispatch path, and sends the command sequence through the aggregate's
//! store. All side effects (raindex deposit/confirm, mint/redemption
//! resume, wrapped-token lookup) live in the aggregate's command handlers
//! via its `Services`.
//!
//! Steps:
//!
//! 1. Claim the `equity_in_progress` guard for the symbol (from absent or
//!    `HeldForRecovery`). If a live transfer owns it (`ActiveTransfer`), the
//!    job reschedules itself with a delay so an unchanged wallet balance is
//!    not stranded waiting for another snapshot.
//! 2. Read the inventory view's wallet balance + active mint/redemption
//!    maps to pick a `DispatchDecision`.
//! 3. Send `Detect` followed by the path-specific command sequence:
//!    `DispatchToMint`, `DispatchToRedemption`, or `SubmitOrphanDeposit +
//!    ConfirmOrphanDeposit`. The aggregate's terminal state encodes the
//!    outcome; no separate completion command is needed.
//!
//! The job carries its `WrappedEquityRecoveryId` in the apalis payload
//! (generated by the reactor when pushing the job) so retries target the
//! same aggregate -- the next command in the sequence picks up wherever
//! the previous attempt stopped.

