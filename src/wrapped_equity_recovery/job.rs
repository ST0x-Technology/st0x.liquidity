/// Apalis queue type for [`WrappedEquityRecoveryJob`].
pub(crate) type WrappedEquityRecoveryJobQueue = JobQueue<WrappedEquityRecoveryJob>;

/// Dependencies the recovery job needs to claim the per-symbol concurrency
/// guard, read inventory, perform the recovery's onchain/transfer side effects,
/// and record outcomes through the aggregate's pure command store.
pub(crate) struct WrappedEquityRecoveryCtx {
    pub(crate) inventory: Arc<BroadcastingInventory>,
    pub(crate) store: Arc<Store<WrappedEquityRecovery>>,
    /// Onchain/transfer dependencies the job drives before recording outcomes.
    pub(crate) services: WrappedEquityRecoveryServices,
    pub(crate) mint_store: Arc<Store<TokenizedEquityMint>>,
    pub(crate) redemption_store: Arc<Store<EquityRedemption>>,
    pub(crate) equity_in_progress: Arc<RwLock<HashMap<Symbol, GuardState>>>,
    /// Queue used for delayed self-reschedule when the shared equity guard is
    /// held by another recovery/transfer job.
    pub(crate) queue: WrappedEquityRecoveryJobQueue,
    pub(crate) reschedule_interval: Duration,
}

/// Why a single recovery job attempt failed. Errors propagate up through
/// apalis. A terminal service failure is recorded as a `RecoveryFailed` event
/// (the attempt returns `Ok`); only retryable failures and business-validation
/// failures surface here.
#[derive(Debug, Error)]
pub(crate) enum WrappedEquityRecoveryJobError {
    #[error("recovery aggregate error: {0}")]
    Aggregate(#[from] SendError<WrappedEquityRecovery>),
    #[error(transparent)]
    Domain(#[from] WrappedEquityRecoveryError),
    #[error(
        "active mint {mint_id} for symbol {symbol} carries quantity {aggregate_shares}, \
         which does not match the wrapped-equity wallet snapshot {snapshot_shares} -- \
         refusing to dispatch to a stale aggregate"
    )]
    ActiveMintQuantityMismatch {
        symbol: Symbol,
        mint_id: IssuerRequestId,
        snapshot_shares: FractionalShares,
        aggregate_shares: FractionalShares,
    },
    #[error(
        "active redemption {redemption_id} for symbol {symbol} carries quantity \
         {aggregate_shares}, which does not match the wrapped-equity wallet snapshot \
         {snapshot_shares} -- refusing to dispatch to a stale aggregate"
    )]
    ActiveRedemptionQuantityMismatch {
        symbol: Symbol,
        redemption_id: RedemptionAggregateId,
        snapshot_shares: FractionalShares,
        aggregate_shares: FractionalShares,
    },
    #[error("active mint {mint_id} for symbol {symbol} is missing from the mint store")]
    ActiveMintMissing {
        symbol: Symbol,
        mint_id: IssuerRequestId,
    },
    #[error(
        "active redemption {redemption_id} for symbol {symbol} is missing from the \
         redemption store"
    )]
    ActiveRedemptionMissing {
        symbol: Symbol,
        redemption_id: RedemptionAggregateId,
    },
    #[error("active mint store error: {0}")]
    ActiveMintAggregate(
        #[from]
        st0x_event_sorcery::AggregateError<
            st0x_event_sorcery::LifecycleError<crate::tokenized_equity_mint::TokenizedEquityMint>,
        >,
    ),
    #[error("active redemption store error: {0}")]
    ActiveRedemptionAggregate(
        #[from]
        st0x_event_sorcery::AggregateError<
            st0x_event_sorcery::LifecycleError<crate::equity_redemption::EquityRedemption>,
        >,
    ),
    #[error(
        "inventory reports no positive wrapped balance for symbol {symbol}; nothing to recover \
         this cycle"
    )]
    NoBalanceSkip { symbol: Symbol },
    #[error(
        "deposit {vault_deposit_tx_hash} was submitted but not yet confirmable; retry from the \
         persisted OrphanDepositSubmitted state"
    )]
    RetryableDepositConfirmation { vault_deposit_tx_hash: TxHash },
    #[error("failed to reschedule recovery job after guard contention: {0}")]
    Reschedule(#[from] QueuePushError),
}

/// Apalis job payload. The reactor pushes one of these per symbol with a
/// positive `BaseWalletWrappedEquity` balance, generating a fresh
/// `recovery_id` at push time so apalis retries hit the same aggregate.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub(crate) struct WrappedEquityRecoveryJob {
    pub(crate) symbol: Symbol,
    pub(crate) recovery_id: WrappedEquityRecoveryId,
}

impl Job<WrappedEquityRecoveryCtx> for WrappedEquityRecoveryJob {
    type Output = ();
    type Error = WrappedEquityRecoveryJobError;

    const WORKER_NAME: &'static str = "wrapped-equity-recovery-worker";

    #[cfg(any(test, feature = "test-support"))]
    const JOB_KIND: crate::conductor::job::JobKind =
        crate::conductor::job::JobKind::WrappedEquityRecovery;

    fn label(&self) -> Label {
        Label::new(format!(
            "WrappedEquityRecovery:{}:{}",
            self.symbol, self.recovery_id
        ))
    }

    async fn perform(&self, ctx: &WrappedEquityRecoveryCtx) -> Result<Self::Output, Self::Error> {
        let symbol = self.symbol.clone();

        // Two-path claim: this job handles a positive `BaseWalletWrapped`
        // balance, which can be either an orphan (absent guard) or the
        // landed-but-unconfirmed `WrapSubmitted` outcome -- the wrap tx actually
        // landed so the tokens are WRAPPED, but the confirmation read failed and
        // the transfer job handed the slot to recovery as `HeldForRecovery`.
        // Claiming `HeldForRecovery` here is what drains that slot. The atomic
        // claim makes a concurrent `UnwrappedEquityRecovery` claim safe -- only
        // one wins, the other reschedules. Returns None only for `ActiveTransfer`
        // (a live transfer job owns the slot).
        let Some(guard) = claim_guard_for_recovery_or_orphan(&ctx.equity_in_progress, &symbol)
        else {
            debug!(
                target: "rebalance",
                %symbol,
                recovery_id = %self.recovery_id,
                "Rescheduling wrapped equity recovery: equity_in_progress held by \
                 active transfer; deferring",
            );
            ctx.queue
                .clone()
                .push_with_delay(self.clone(), ctx.reschedule_interval)
                .await?;
            return Ok(());
        };

        let current_state = ctx.store.load(&self.recovery_id).await?;

        if let Some(state) = &current_state
            && state.is_terminal()
        {
            debug!(
                target: "rebalance",
                %symbol,
                recovery_id = %self.recovery_id,
                "Skipping wrapped equity recovery: aggregate already terminal",
            );
            guard.release();
            return Ok(());
        }

        let result = match current_state {
            None => start_from_detect(ctx, &self.recovery_id, &symbol).await,
            Some(WrappedEquityRecovery::Detected { shares, .. }) => {
                resume_from_detected(ctx, &self.recovery_id, &symbol, shares).await
            }
            Some(WrappedEquityRecovery::OrphanDepositSubmitted {
                vault_deposit_tx_hash,
                ..
            }) => resume_from_confirm_deposit(ctx, &self.recovery_id, vault_deposit_tx_hash).await,
            // Terminal states are short-circuited by the `is_terminal` early
            // return above; enumerated explicitly (not `_`) so a new non-terminal
            // state fails to compile until it is wired into a resume path.
            Some(
                WrappedEquityRecovery::DispatchedToMint { .. }
                | WrappedEquityRecovery::DispatchedToRedemption { .. }
                | WrappedEquityRecovery::OrphanDeposited { .. }
                | WrappedEquityRecovery::Failed { .. },
            ) => Ok(()),
        };

        // - Ok: release the guard (removes the slot); recovery is done.
        // - NoBalanceSkip: map to Ok so apalis does NOT retry; the guard drops to
        //   its origin. A HeldForRecovery-origin skip self-reschedules since the
        //   reactor only re-dispatches on a positive balance.
        // - other Err: propagate; the guard drops, restoring its prior state.
        match result {
            Ok(()) => {
                guard.release();
                Ok(())
            }
            Err(WrappedEquityRecoveryJobError::NoBalanceSkip { symbol: ref sym }) => {
                if guard.claimed_from_held_for_recovery() {
                    debug!(
                        target: "rebalance",
                        symbol = %sym,
                        recovery_id = %self.recovery_id,
                        "Rescheduling wrapped equity recovery: no positive balance yet but slot \
                         is HeldForRecovery; re-enqueueing with delay to keep polling until the \
                         balance resolves or the slot is cleared",
                    );
                    ctx.queue
                        .clone()
                        .push_with_delay(self.clone(), ctx.reschedule_interval)
                        .await?;
                } else {
                    debug!(
                        target: "rebalance",
                        symbol = %sym,
                        recovery_id = %self.recovery_id,
                        "Skipping wrapped equity recovery: no positive balance in inventory view; \
                         orphan claim, guard drop removes entry so future inventory polls can \
                         re-trigger recovery",
                    );
                }
                Ok(())
            }
            Err(other) => Err(other),
        }
    }
}

/// Fresh recovery: read inventory, validate active-aggregate quantity, send
/// `Detect`, then dispatch from `Detected`.
async fn start_from_detect(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    symbol: &Symbol,
) -> Result<(), WrappedEquityRecoveryJobError> {
    let Some(snapshot) = read_recovery_snapshot(&ctx.inventory, symbol).await else {
        debug!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            "Skipping wrapped equity recovery: no positive balance in inventory view",
        );
        return Err(WrappedEquityRecoveryJobError::NoBalanceSkip {
            symbol: symbol.clone(),
        });
    };

    validate_active_aggregate_quantity(ctx, symbol, &snapshot).await?;

    ctx.store
        .send(
            recovery_id,
            WrappedEquityRecoveryCommand::Detect {
                symbol: symbol.clone(),
                shares: snapshot.shares,
            },
        )
        .await?;

    info!(
        target: "rebalance",
        %symbol,
        %recovery_id,
        shares = %snapshot.shares,
        ?snapshot.dispatch,
        "Wrapped equity recovery: dispatched detection",
    );

    dispatch_from_detected(ctx, recovery_id, symbol, snapshot.shares, snapshot.dispatch).await
}

/// Resume from `Detected`: a previous attempt persisted `Detect` but crashed
/// before any dispatch command landed. Re-read inventory and dispatch, failing
/// the stale aggregate if the balance vanished or its share count changed.
async fn resume_from_detected(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    symbol: &Symbol,
    detected_shares: FractionalShares,
) -> Result<(), WrappedEquityRecoveryJobError> {
    let Some(snapshot) = read_recovery_snapshot(&ctx.inventory, symbol).await else {
        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            "Resuming wrapped recovery from Detected but inventory now reports zero balance; \
             failing the aggregate to free the symbol",
        );
        return fail_recovery(
            ctx,
            recovery_id,
            "inventory reports zero wrapped balance on resume from Detected".to_string(),
        )
        .await;
    };

    if snapshot.shares != detected_shares {
        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            snapshot_shares = %snapshot.shares,
            %detected_shares,
            "Resuming wrapped recovery from Detected but inventory shares changed; \
             failing the stale aggregate to allow a fresh recovery",
        );
        return fail_recovery(
            ctx,
            recovery_id,
            format!(
                "inventory shares changed on resume from Detected: detected {detected_shares}, \
                 current {}",
                snapshot.shares
            ),
        )
        .await;
    }

    // Re-validate against the fresh snapshot: a crash can land here after the
    // inventory shifted, so do not trust the persisted `Detected` premise.
    if let Err(error) = validate_active_aggregate_quantity(ctx, symbol, &snapshot).await {
        if !is_business_validation_error(&error) {
            return Err(error);
        }

        warn!(
            target: "rebalance",
            %symbol,
            %recovery_id,
            %error,
            "Wrapped equity recovery: active aggregate quantity mismatch on resume; \
             failing recovery",
        );
        return fail_recovery(
            ctx,
            recovery_id,
            format!("active aggregate validation failed on resume: {error}"),
        )
        .await;
    }

    dispatch_from_detected(ctx, recovery_id, symbol, snapshot.shares, snapshot.dispatch).await
}

/// Performs the dispatch step's I/O and records the outcome. For active
/// mint/redemption, resumes the transfer and records the dispatch (or
/// `RecoveryFailed`); for the orphan path, deposits the already-wrapped tokens;
/// for conflict, fails the aggregate.
async fn dispatch_from_detected(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    symbol: &Symbol,
    shares: FractionalShares,
    dispatch: DispatchDecision,
) -> Result<(), WrappedEquityRecoveryJobError> {
    match dispatch {
        DispatchDecision::ActiveMint(mint_id) => {
            match ctx.services.transfer.resume_mint(&mint_id).await {
                Ok(()) => {
                    info!(target: "rebalance", %mint_id, "Wrapped equity recovery: resume_mint succeeded");
                    ctx.store
                        .send(
                            recovery_id,
                            WrappedEquityRecoveryCommand::DispatchToMint { mint_id },
                        )
                        .await?;
                    Ok(())
                }
                Err(error) => {
                    warn!(target: "rebalance", %mint_id, ?error, "Wrapped equity recovery: resume_mint failed");
                    fail_recovery(ctx, recovery_id, format!("resume_mint failed: {error}")).await
                }
            }
        }
        DispatchDecision::ActiveRedemption(redemption_id) => {
            match ctx
                .services
                .transfer
                .resume_redemption(&redemption_id)
                .await
            {
                Ok(()) => {
                    info!(target: "rebalance", %redemption_id, "Wrapped equity recovery: resume_redemption succeeded");
                    ctx.store
                        .send(
                            recovery_id,
                            WrappedEquityRecoveryCommand::DispatchToRedemption { redemption_id },
                        )
                        .await?;
                    Ok(())
                }
                Err(error) => {
                    warn!(target: "rebalance", %redemption_id, ?error, "Wrapped equity recovery: resume_redemption failed");
                    fail_recovery(
                        ctx,
                        recovery_id,
                        format!("resume_redemption failed: {error}"),
                    )
                    .await
                }
            }
        }
        DispatchDecision::Orphan => {
            resume_from_submit_deposit(ctx, recovery_id, symbol, shares).await
        }
        DispatchDecision::Conflict {
            mint_id,
            redemption_id,
        } => {
            let reason = format!(
                "InventoryView reports active mint {mint_id} and active redemption \
                 {redemption_id} for symbol {symbol}; refusing to dispatch"
            );
            warn!(target: "rebalance", %symbol, %mint_id, %redemption_id, "Wrapped equity recovery: conflict");
            fail_recovery(ctx, recovery_id, reason).await
        }
    }
}

/// Orphan path step 1: resolve the wrapped token + vault, submit the deposit,
/// record the tx hash, then chain to confirm. The tokens are already wrapped
/// (wtSTOCK in the wallet), so there is no wrap step.
async fn resume_from_submit_deposit(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    symbol: &Symbol,
    shares: FractionalShares,
) -> Result<(), WrappedEquityRecoveryJobError> {
    let wrapped_token = match ctx.services.wrapper.lookup_derivative(symbol) {
        Ok(token) => token,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Wrapped equity recovery: lookup_derivative failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("wrapper.lookup_derivative failed: {error}"),
            )
            .await;
        }
    };

    let vault_id = match ctx
        .services
        .vault_lookup
        .vault_id_for_token(wrapped_token)
        .await
    {
        Ok(id) => id,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Wrapped equity recovery: vault_id_for_token failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("vault_lookup.vault_id_for_token failed: {error}"),
            )
            .await;
        }
    };

    let raw = match shares.to_u256_18_decimals() {
        Ok(raw) => raw,
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Wrapped equity recovery: shares conversion failed");
            return fail_recovery(
                ctx,
                recovery_id,
                format!("shares conversion failed: {error}"),
            )
            .await;
        }
    };

    match ctx
        .services
        .raindex
        .submit_deposit(wrapped_token, vault_id, raw, TOKENIZED_EQUITY_DECIMALS)
        .await
    {
        Ok(vault_deposit_tx_hash) => {
            info!(target: "rebalance", %symbol, %vault_deposit_tx_hash, "Wrapped equity recovery: submit_deposit succeeded");
            ctx.store
                .send(
                    recovery_id,
                    WrappedEquityRecoveryCommand::SubmitOrphanDeposit {
                        vault_deposit_tx_hash,
                    },
                )
                .await?;
            resume_from_confirm_deposit(ctx, recovery_id, vault_deposit_tx_hash).await
        }
        Err(error) => {
            warn!(target: "rebalance", %symbol, ?error, "Wrapped equity recovery: submit_deposit failed");
            fail_recovery(
                ctx,
                recovery_id,
                format!("raindex.submit_deposit failed: {error}"),
            )
            .await
        }
    }
}

/// Orphan path step 2: confirm the Raindex deposit and record it. A dropped tx
/// is terminal; any other confirmation error is retryable so apalis re-runs from
/// the persisted `OrphanDepositSubmitted` without re-depositing.
async fn resume_from_confirm_deposit(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    vault_deposit_tx_hash: TxHash,
) -> Result<(), WrappedEquityRecoveryJobError> {
    match ctx.services.raindex.confirm_tx(vault_deposit_tx_hash).await {
        Ok(()) => {
            info!(target: "rebalance", %vault_deposit_tx_hash, "Wrapped equity recovery: confirm_tx succeeded");
            ctx.store
                .send(
                    recovery_id,
                    WrappedEquityRecoveryCommand::ConfirmOrphanDeposit,
                )
                .await
                .map_err(Into::into)
        }
        Err(error) => {
            warn!(target: "rebalance", %vault_deposit_tx_hash, ?error, "Wrapped equity recovery: confirm_tx failed");
            if error.is_transaction_dropped() {
                return fail_recovery(
                    ctx,
                    recovery_id,
                    format!("raindex.confirm_tx failed: {error}"),
                )
                .await;
            }
            Err(
                WrappedEquityRecoveryJobError::RetryableDepositConfirmation {
                    vault_deposit_tx_hash,
                },
            )
        }
    }
}

/// Records a terminal recovery failure as a `RecoveryFailed` event. Returns
/// `Ok(())` so the caller stops the chain without burning an apalis retry on an
/// already-terminal aggregate.
async fn fail_recovery(
    ctx: &WrappedEquityRecoveryCtx,
    recovery_id: &WrappedEquityRecoveryId,
    reason: String,
) -> Result<(), WrappedEquityRecoveryJobError> {
    ctx.store
        .send(
            recovery_id,
            WrappedEquityRecoveryCommand::FailRecovery { reason },
        )
        .await?;
    Ok(())
}

/// Distinguishes stale-premise business-validation failures (the active
/// mint/redemption no longer matches the wallet snapshot) from infrastructure
/// errors. On resume, the former terminalize the aggregate; the latter retry.
fn is_business_validation_error(error: &WrappedEquityRecoveryJobError) -> bool {
    match error {
        WrappedEquityRecoveryJobError::ActiveMintQuantityMismatch { .. }
        | WrappedEquityRecoveryJobError::ActiveRedemptionQuantityMismatch { .. }
        | WrappedEquityRecoveryJobError::ActiveMintMissing { .. }
        | WrappedEquityRecoveryJobError::ActiveRedemptionMissing { .. } => true,
        WrappedEquityRecoveryJobError::Aggregate(_)
        | WrappedEquityRecoveryJobError::Domain(_)
        | WrappedEquityRecoveryJobError::ActiveMintAggregate(_)
        | WrappedEquityRecoveryJobError::ActiveRedemptionAggregate(_)
        | WrappedEquityRecoveryJobError::Reschedule(_)
        | WrappedEquityRecoveryJobError::RetryableDepositConfirmation { .. }
        | WrappedEquityRecoveryJobError::NoBalanceSkip { .. } => false,
    }
}

struct RecoverySnapshot {
    shares: st0x_execution::FractionalShares,
    dispatch: DispatchDecision,
}

#[derive(Debug)]
enum DispatchDecision {
    ActiveMint(IssuerRequestId),
    ActiveRedemption(RedemptionAggregateId),
    Orphan,
    /// `InventoryView` reports both an active mint and an active redemption
    /// for the same symbol. A symbol can only be in one in-flight transfer
    /// at a time -- this is a corrupt-state signal, not a normal dispatch
    /// path.
    Conflict {
        mint_id: IssuerRequestId,
        redemption_id: RedemptionAggregateId,
    },
}

async fn read_recovery_snapshot(
    inventory: &BroadcastingInventory,
    symbol: &Symbol,
) -> Option<RecoverySnapshot> {
    let view = inventory.read().await;
    let shares = view.inflight_equity_at(symbol, InFlightEquityLocation::BaseWalletWrapped)?;
    if shares == st0x_execution::FractionalShares::ZERO {
        return None;
    }
    let dispatch = decide_dispatch(&view, symbol);
    drop(view);
    Some(RecoverySnapshot { shares, dispatch })
}

/// Confirms the active mint or redemption aggregate carries the same quantity
/// as the wrapped wallet snapshot. Without this check, the recovery stream
/// would record `snapshot.shares` for an aggregate that was actually set up
/// for a different amount, leaving the audit trail and the resumed work
/// disagreeing on how many shares moved.
async fn validate_active_aggregate_quantity(
    ctx: &WrappedEquityRecoveryCtx,
    symbol: &Symbol,
    snapshot: &RecoverySnapshot,
) -> Result<(), WrappedEquityRecoveryJobError> {
    match &snapshot.dispatch {
        DispatchDecision::ActiveMint(mint_id) => {
            let mint = ctx.mint_store.load(mint_id).await?.ok_or_else(|| {
                WrappedEquityRecoveryJobError::ActiveMintMissing {
                    symbol: symbol.clone(),
                    mint_id: mint_id.clone(),
                }
            })?;
            let aggregate_shares = FractionalShares::new(mint.quantity());
            if aggregate_shares != snapshot.shares {
                return Err(WrappedEquityRecoveryJobError::ActiveMintQuantityMismatch {
                    symbol: symbol.clone(),
                    mint_id: mint_id.clone(),
                    snapshot_shares: snapshot.shares,
                    aggregate_shares,
                });
            }
            Ok(())
        }
        DispatchDecision::ActiveRedemption(redemption_id) => {
            let redemption = ctx
                .redemption_store
                .load(redemption_id)
                .await?
                .ok_or_else(|| WrappedEquityRecoveryJobError::ActiveRedemptionMissing {
                    symbol: symbol.clone(),
                    redemption_id: redemption_id.clone(),
                })?;
            let aggregate_shares = FractionalShares::new(redemption.quantity());
            if aggregate_shares != snapshot.shares {
                return Err(
                    WrappedEquityRecoveryJobError::ActiveRedemptionQuantityMismatch {
                        symbol: symbol.clone(),
                        redemption_id: redemption_id.clone(),
                        snapshot_shares: snapshot.shares,
                        aggregate_shares,
                    },
                );
            }
            Ok(())
        }
        DispatchDecision::Orphan | DispatchDecision::Conflict { .. } => Ok(()),
    }
}

fn decide_dispatch(view: &InventoryView, symbol: &Symbol) -> DispatchDecision {
    match (view.active_mint(symbol), view.active_redemption(symbol)) {
        (Some(mint_id), Some(redemption_id)) => DispatchDecision::Conflict {
            mint_id: mint_id.clone(),
            redemption_id: redemption_id.clone(),
        },
        (Some(mint_id), None) => DispatchDecision::ActiveMint(mint_id.clone()),
        (None, Some(redemption_id)) => DispatchDecision::ActiveRedemption(redemption_id.clone()),
        (None, None) => DispatchDecision::Orphan,
    }
}

